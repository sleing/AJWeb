package org.ddd.serialize.example15;

public class Chapter {
	private int no;
	private String title;

	public Chapter() {
		super();
	}
	public Chapter(int no, String title) {
		super();
		this.no = no;
		this.title = title;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	
}
